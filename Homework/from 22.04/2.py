d = {
    "q0A": ["A", "S", "q0"],
    "q00": ["0", "R", "q1"],
    "q01": ["1", "L", "q1"],
    "q02": ["1", "N", "q1"],

    "q1A": ["A", "R", "q2"],
    "q10": ["1", "R", "q2"],
    "q11": ["5", "R", "q2"],
    "q12": ["0", "R", "q2"],

    "q2A": ["A", "R", "q0"],
    "q20": ["0", "R", "q0"],
    "q21": ["1", "R", "q0"],
    "q22": ["4", "R", "q3"],

    "q3A": ["2", "R", "q0"],
    "q30": ["0", "R", "q1"],
    "q31": ["1", "R", "q0"],
    "q32": ["2", "L", "q0"],
}

s = ["A"] + list("012" * 400) + ["A"] * 100
q = "q0"
i = 1
c = None

while c != "S":
    s[i], c, q = d[q + s[i]]
    if c == "R":
        i += 1
    elif c == "L":
        i -= 1

print(sum(int(x) for x in s if x != "A"))